value =  '10' + 1
console.log("'10' + 1: " + value) //'10' + 1: 101

value =  1 + '10'
console.log("1 + '10': " + value) //1 + '10': 110

value =  1 + +'10'
console.log("1 + +'10': " + value) //1 + +'10': 11

value =  1 - '10'
console.log("1 - '10': " + value) //1 - '10': -9

value =  '10' - 1
console.log("'10' - 1: " + value) //'10' - 1: 9

value =  +"10"
console.log('+"10": '+ value) //+"10": 10

value =  -"10"
console.log('-"10": '+ value) //-"10": -10 
